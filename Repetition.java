public class Repetition {
//    主方法
    public static void main(String args[]){
//        申明数组
        int arr[] = {6,10,1,12,33};
        System.out.println("一维数组: ");
//        foreach 语句，int x 应用变量，arr指定要循环遍历的数组，最后将x输出
//        python 中 for  int x  in  arr:
        for (int x : arr){
            System.out.println(x);
        }

    }
}
