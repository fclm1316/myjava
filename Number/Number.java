package Number;

public class Number {
//  方法修饰符  静态修饰符   返回值修饰符  ->> main()方法的
    public static void main(String[] args){
        byte mybyte = 124;
        short myshort = 32564;
        int myint = 2345566;
        long mylong = 456900;
        long result = mybyte + myshort + myint + mylong;
        System.out.println(result);
    }
}
